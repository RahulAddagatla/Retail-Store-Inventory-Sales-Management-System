-- Top 3 Revenue-Generating Products

SELECT p.ProductName, SUM(od.Quantity * od.Priceperunit) AS TotalRevenue 

from OrderDetails od
JOIN Products p ON od.ProductID = p.ProductID
GROUP BY p.ProductName
ORDER BY TotalRevenue DESC
LIMIT 3;


-- Customers Who Spent More Than the Average Customer

SELECT c.FirstName, c.LastName, SUM(o.TotalAmount)  AS TotalSpent
FROM Customers c
JOIN Orders o ON c.CustomerID = o.CustomerID
GROUP BY c.CustomerID
HAVING TotalSpent > (
SELECT AVG(TotalAmount) FROM Orders
);

-- Monthly Sales Trends

SELECT 
    DATE_FORMAT(OrderDate, '%Y-%m') AS Month, 
    COUNT(OrderID) AS TotalOrders, 
    SUM(TotalAmount) AS MonthlyRevenue
FROM Orders
GROUP BY DATE_FORMAT(OrderDate, '%Y-%m')
ORDER BY Month;

-- Write a query to find the total number of unique products in each Category."

SELECT 
      Category,
      COUNT(ProductID) AS TotalUniqueProducts
FROM Products
GROUP BY Category;


-- Write a query to find how many orders were placed on each specific date, 
-- sorted from the busiest day to the slowest."

SELECT 
    OrderDate,
    COUNT(OrderID) AS OrdersPlaced
FROM Orders
GROUP BY OrderDate
ORDER BY OrdersPlaced DESC;    

-- Write a query to show the total revenue generated by each product

SELECT 
    p.Category,
    SUM(od.Quantity * od.PricePerUnit) AS TotalCategoryRevenue
From OrderDetails od
JOIN Products p ON od.ProductID = p.ProductID
GROUP BY p.Category
ORDER BY TotalCategoryRevenue DESC;

-- Write a query that displays each customer's FirstName, LastName, and the total number of orders they have placed."

SELECT 
    c.FirstName, 
    c.LastName, 
    COUNT(o.OrderID) AS TotalOrdersPlaced
FROM Customers c
LEFT JOIN Orders o ON c.CustomerID = o.CustomerID
GROUP BY c.CustomerID, c.FirstName, c.LastName
ORDER BY TotalOrdersPlaced DESC;

-- Write a query to find all customers who have spent a grand total of more than $500 across all their orders combined."

SELECT 
    c.CustomerID,
    c.FirstName, 
    c.LastName, 
    SUM(o.TotalAmount) AS GrandTotalSpent
FROM Customers c
JOIN Orders o ON c.CustomerID = o.CustomerID
GROUP BY c.CustomerID, c.FirstName, c.LastName
HAVING GrandTotalSpent > 500.00
ORDER BY GrandTotalSpent DESC;